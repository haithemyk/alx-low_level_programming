#include "3-calc.h"
/**
* op_add - will calculate sum of a and b
* @a: the first arg as integer
* @b: the size of array
* Return:the sum of a and b
*/
int op_add(int a, int b)
{
return (a + b);
}
/**
* op_sub - will calculate substitution of a and b
* @a: the first arg as integer
* @b: the size of array
* Return:the sub of a and b
*/
int op_sub(int a, int b)
{
return (a - b);
}
/**
* op_mul - will calculate multiplucation of a and b
* @a: the first arg as integer
* @b: the second arg as integer
* Return:the mul of a and b
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
* op_mod - will calculate modulo of a and b
* @a: the first arg as integer
* @b: the second arg as integer
* Return:the mod of a and b
*/
int op_mod(int a, int b)
{
return (a % b);
}

/**
* op_div - will calculate division of a and b
* @a: the first arg as integer
* @b: the second arg as integer
* Return:the div of a and b
*/
int op_div(int a, int b)
{
return (a / b);
}
